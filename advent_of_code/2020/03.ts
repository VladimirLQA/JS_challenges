export function countTrees2(area: string[]): number {
  const slopes = [
    { right: 1, down: 1 },
    { right: 3, down: 1 },
    { right: 5, down: 1 },
    { right: 7, down: 1 },
    { right: 1, down: 2 },
  ];

  return slopes.reduce((sum, { right, down }) => {
    let treesCount = 0;
    let col = 0;

    for (let i = 0; i <= area.length - 1; i += down) {
      const row = area[i];
      const wrappedCol = col % row.length;
      if (row[wrappedCol] === '#') treesCount += 1;
      col += right;
    }
    return sum * treesCount;
  }, 1);
}

