export function countTrees(area: string[]): number {
  let treesCount = 0;
  let col = 0;

  for (let i = 0; i <= area.length - 1; i += 1) {
    const row = area[i];
    const wrappedCol = col % row.length;
    if (row[wrappedCol] === '#') treesCount += 1;
    col += 3;
  }

  return treesCount;
}

